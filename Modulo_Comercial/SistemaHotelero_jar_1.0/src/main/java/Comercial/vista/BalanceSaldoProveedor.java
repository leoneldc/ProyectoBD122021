    void cargartodasfacturas()
    {
        
        DefaultTableModel tabla= new DefaultTableModel();
        String []titulos={"Codi.documento","Codi.proveedor","Transaccional","Fecha_emision","Fecha_atraso","Dias vencidos","total detalle"};
        tabla.setColumnIdentifiers(titulos);
        this.tbfacturas.setModel(tabla);
        String consulta= "SELECT * FROM tbl_balance_saldo_proveedores";
        String []Datos= new String [7];
        try {
            Statement st = cn.createStatement();
            ResultSet rs= st.executeQuery(consulta);
            while(rs.next())
            {
                Datos[0]=rs.getString("PK_codigo_documento");
                Datos[1]=rs.getString("codigo_proveedor");
                Datos[2]=rs.getString("transaccion");
                Datos[3]=rs.getString("fecha_emision");
                Datos[4]=rs.getString("fecha_atraso");
                Datos[5]=rs.getString("dias_vencidos");
                Datos[6]=rs.getString("total_detalle");
                tabla.addRow(Datos);
            }
        } catch (SQLException ex) {
            Logger.getLogger(ListadoProductos.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

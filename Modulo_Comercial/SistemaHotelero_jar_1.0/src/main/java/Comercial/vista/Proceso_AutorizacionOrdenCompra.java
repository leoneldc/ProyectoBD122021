    private void Btn_GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_GuardarActionPerformed
        // TODO add your handling code here:

        AutorizacionOrdenCompraDAO proveedorDAO = new AutorizacionOrdenCompraDAO();
        AutorizacionOrdenCompra1 insertar = new AutorizacionOrdenCompra1();
        insertar.setPK_codigo_autorizacion((int) Integer.parseInt(Txt_codigo.getText()));
        insertar.setCodigo_ordencompra(Txt_codigo_orden.getText());
        insertar.setNombre_autorizacion(Txt_nombre.getText());
        insertar.setDepartamento_autorizacion(txt_puesto.getSelectedItem().toString());
        insertar.setFecha_autorizacion(Txt_fecha.getText());
        insertar.setEstatus_autorizacion(Txt_estado.getText());
        proveedorDAO.insert(insertar);

        llenadoDeTablas();
        limpiar();

    }//GEN-LAST:event_Btn_GuardarActionPerformed


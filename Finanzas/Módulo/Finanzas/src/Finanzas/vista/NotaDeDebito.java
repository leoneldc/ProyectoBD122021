        insertarcuenta.setNumeroDeDocumento(TxtNumeroDocumento.getText());
        insertarcuenta.setNumeroDeCuenta(TxtCuenta.getText());
        insertarcuenta.setBeneficiario(TxtBeneficiario.getText());
        insertarcuenta.setFecha(TxtFecha.getText());

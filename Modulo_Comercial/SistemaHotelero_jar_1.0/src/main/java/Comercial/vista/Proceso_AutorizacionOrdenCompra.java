    public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Codigo");
        modelo.addColumn("CodigoOrdenCompra");
        modelo.addColumn("Nombre");
        modelo.addColumn("Puesto");
        modelo.addColumn("Fecha");
        modelo.addColumn("Estado");
        
        AutorizacionOrdenCompraDAO proveedorDAO = new AutorizacionOrdenCompraDAO();
        List<AutorizacionOrdenCompra1> proveedor = proveedorDAO.select();
        Tbl_Datos.setModel(modelo);
        String[] dato = new String[6];
        for (int i = 0; i < proveedor.size(); i++) {
            dato[0] = Integer.toString(proveedor.get(i).getPK_codigo_autorizacion());
            dato[1] = proveedor.get(i).getCodigo_ordencompra();
            dato[2] = proveedor.get(i).getNombre_autorizacion();
            dato[3] = proveedor.get(i).getDepartamento_autorizacion();
            dato[4] = proveedor.get(i).getFecha_autorizacion();
            dato[5] = proveedor.get(i).getEstatus_autorizacion();
            //System.out.println("vendedor:" + vendedores);
            modelo.addRow(dato);
        }
    }

    public void buscar() {
        AutorizacionOrdenCompra1 proveedorAConsultar = new AutorizacionOrdenCompra1();
        AutorizacionOrdenCompraDAO proveedorDAO = new AutorizacionOrdenCompraDAO();
        proveedorAConsultar.setPK_codigo_autorizacion(Integer.parseInt(Txt_codigo.getText()));
        proveedorAConsultar = proveedorDAO.query(proveedorAConsultar);

        Txt_codigo_orden.setText(proveedorAConsultar.getCodigo_ordencompra());
        Txt_nombre.setText(proveedorAConsultar.getNombre_autorizacion());
        txt_puesto.setSelectedItem(proveedorAConsultar.getDepartamento_autorizacion());
        Txt_fecha.setText(proveedorAConsultar.getFecha_autorizacion());
        Txt_estado.setText(proveedorAConsultar.getEstatus_autorizacion());

    }

